export function t(key: string, locale: Locale = 'de'): string {
  return dict[locale]?.[key] ?? dict.de[key] ?? key
}

/**
 * Read the host app's locale from the <html lang="..."> attribute. Falls back
 * to 'de' since SelfDashboard defaults to German.
 */
export function detectLocale(): Locale {
  if (typeof document === 'undefined') return 'de'
  const lang = document.documentElement.lang?.toLowerCase() ?? ''
  return lang.startsWith('en') ? 'en' : 'de'
}
